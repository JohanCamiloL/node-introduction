/**
 * Sum two numbers.
 * @param {Number} a Number a.
 * @param {Number} b Number b.
 * @returns Numbers sum.
 */
const sum = (a, b) => a + b;

/**
 * Substract two numbers.
 * @param {Number} a Number a.
 * @param {Number} b Number b.
 * @returns Numbers substraction.
 */
const sub = (a, b) => a - b;

module.exports = { sum };